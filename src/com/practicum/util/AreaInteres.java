package com.practicum.util;

public class AreaInteres {
	
	private int id;
	private int run;
	private int idCurriculum;
	private String area;
	private int desarrollo;
	private int baseDeDatos;
	private int redes;
	
	public int getId() {
		return id;
	}
	
	public int getRun() {
		return run;
	}

	public void setRun(int run) {
		this.run = run;
	}

	public int getIdCurriculum() {
		return idCurriculum;
	}

	public void setIdCurriculum(int idCurriculum) {
		this.idCurriculum = idCurriculum;
	}

	public void setId(int id) {
		this.id = id;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}

	public int getDesarrollo() {
		return desarrollo;
	}

	public void setDesarrollo(int desarrollo) {
		this.desarrollo = desarrollo;
	}

	public int getBaseDeDatos() {
		return baseDeDatos;
	}

	public void setBaseDeDatos(int baseDeDatos) {
		this.baseDeDatos = baseDeDatos;
	}

	public int getRedes() {
		return redes;
	}

	public void setRedes(int redes) {
		this.redes = redes;
	}
	
}
