package com.practicum.util;

public class Asignatura {
	private String asignatura;
	private float nota;
	private float desarrollo;
	private float baseDeDatos;
	private float redes;
	
	public String getAsignatura() {
		return asignatura;
	}
	public void setAsignatura(String asignatura) {
		this.asignatura = asignatura;
	}
	public float getNota() {
		return nota;
	}
	public void setNota(float nota) {
		this.nota = nota;
	}
	public float getDesarrollo() {
		return desarrollo;
	}
	public void setDesarrollo(float desarrollo) {
		this.desarrollo = desarrollo;
	}
	public float getBaseDeDatos() {
		return baseDeDatos;
	}
	public void setBaseDeDatos(float baseDeDatos) {
		this.baseDeDatos = baseDeDatos;
	}
	public float getRedes() {
		return redes;
	}
	public void setRedes(float redes) {
		this.redes = redes;
	}
	
	
}
